    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Order $order)
    {
        return view('backend.orders.show')->with([
            'order' => $order,
            'images' => Image::where('product_id', $order->product_id)->get(),
        ]);
    }

